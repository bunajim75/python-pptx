def test_sha256_file_calculates_digest_from_file_bytes(tmp_path):
    path = tmp_path / "fixture.bin"
    path.write_bytes(b"frozen fixture")

    assert sha256_file(path) == hashlib.sha256(b"frozen fixture").hexdigest()


def test_manifest_parser_rejects_duplicate_paths(tmp_path):
    manifest = tmp_path / "MANIFEST.sha256"
    manifest.write_text(
        f"{'0' * 64}  powerpoint-authored/example.pptx\n"
        f"{'1' * 64}  powerpoint-authored/example.pptx\n",
        encoding="utf-8",
    )

    with pytest.raises(AssertionError, match="duplicate manifest path"):
        parse_manifest(manifest)


