def _populated_corpus(
    tmp_path: Path, *, role: str = "powerpoint-authored", scenario: str = "example"
) -> Path:
    corpus_root = tmp_path / "layout-cloning"
    fixture_directory = corpus_root / role
    fixture_directory.mkdir(parents=True)
    (corpus_root / "README.md").write_text("temporary convention", encoding="utf-8")
    (fixture_directory / f"{scenario}.pptx").write_bytes(b"temporary PPTX stand-in")
    (fixture_directory / f"{scenario}.json").write_text(
        json.dumps(_sidecar(role=role)), encoding="utf-8"
    )
    (fixture_directory / f"{scenario}.desktop.json").write_text(
        json.dumps(
            _desktop_verification(status="not-run" if role == "synthetic-invalid" else "passed")
        ),
        encoding="utf-8",
    )
    _write_manifest(corpus_root)
    return corpus_root


def _sidecar(*, role: str = "powerpoint-authored") -> dict[str, object]:
    return {
        "schema_version": 1,
        "role": role,
        "date": "2026-07-21",
        "description": "Temporary package for corpus-validator self-tests.",
        "source": "Created from scratch for this test.",
        "application": None if role == "synthetic-invalid" else "Microsoft PowerPoint",
    }


def _desktop_verification(*, status: str = "passed") -> dict[str, object]:
    completed = status != "not-run"
    return {
        "schema_version": 1,
        "status": status,
        "date": "2026-07-21" if completed else None,
        "application": "Microsoft PowerPoint" if completed else None,
        "version": "test-version" if completed else None,
        "platform": "test-platform" if completed else None,
        "notes": "Temporary validator self-test.",
    }


def _write_manifest(corpus_root: Path) -> None:
    entries = {
        path.relative_to(corpus_root).as_posix(): sha256_file(path)
        for path in corpus_root.rglob("*")
        if path.is_file() and path.name not in {"MANIFEST.sha256", "README.md"}
    }
    _write_manifest_entries(corpus_root, entries)


def _write_manifest_entries(corpus_root: Path, entries: dict[str, str]) -> None:
    lines = [f"{digest}  {path}\n" for path, digest in sorted(entries.items())]
    (corpus_root / "MANIFEST.sha256").write_text("".join(lines), encoding="utf-8")
