_CORPUS_ROOT = Path(test_file_dir) / "pptx" / "layout-cloning"


def test_frozen_repository_corpus_controls_are_valid():
    validate_fixture_corpus(_CORPUS_ROOT)


def test_repository_corpus_is_deliberately_empty_and_forbids_generated_ground_truth():
    manifest = (_CORPUS_ROOT / "MANIFEST.sha256").read_text(encoding="utf-8")
    readme = (_CORPUS_ROOT / "README.md").read_text(encoding="utf-8")

    assert manifest.splitlines() == ["# EMPTY: no fixture artifacts are committed yet."]
    assert "must not be generated by the production code under test" in " ".join(readme.split())


def test_empty_corpus_without_explicit_marker_is_rejected(tmp_path):
    corpus_root = tmp_path / "layout-cloning"
    corpus_root.mkdir()
    (corpus_root / "README.md").write_text("temporary convention", encoding="utf-8")
    (corpus_root / "MANIFEST.sha256").write_text("# merely blank\n", encoding="utf-8")

    with pytest.raises(AssertionError, match="empty corpus must contain"):
        validate_fixture_corpus(corpus_root)


